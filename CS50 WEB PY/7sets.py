# Create an empty set

s = set()

# Add elements

s.add(223)
s.add(45)
s.add(56)
s.add(5)
s.add(956)
s.add(5)



# Remove an item

s.remove(223)

print(s)

# Length of the set

l = len(s)

print(f"The set has {l} elements.")




























