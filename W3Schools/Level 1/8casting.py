# Integers:

x = int(1)   # x will be 1
y = int(2.8) # y will be 2
z = int("3") # z will be 3



#Floats:

a = float(1)     # a will be 1.0
b = float(2.8)   # b will be 2.8
c = float("3")   # c will be 3.0
d = float("4.2") # d will be 4.2



# Strings:

i = str("s1") # i will be 's1'
j = str(2)    # j will be '2'
k = str(3.0)  # k will be '3.0'

g = str("jumbo")